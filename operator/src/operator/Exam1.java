package operator;

public class Exam1 {
	public static void main(String[] args) {
		int data1 = 9, data2 = 2;
		
		
		System.out.println(data1 + "+" + data2 + "=" + (data1+data2));
		System.out.println(data1 + "-" + data2 + "=" + (data1-data2));
		System.out.println(data1 + "*" + data2 + "=" + (data1*data2));
		System.out.println(data1 + "/" + data2 + "=" + (data1/data2));  //int로 하면 정수 4만 나옴 둘 중 하나가 실수면 4.5나옴
		System.out.println(data1 + "%" + data2 + "=" + (data1%data2));
		
		
		
	}
}
